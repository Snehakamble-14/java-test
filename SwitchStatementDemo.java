class SwitchStatementDemo{
public static void main(String args[]){
int number = (3);
switch(number){
case 1: System.out.println("1st day of a week is Sunday");
break;
case 2: System.out.println("2nd day of a week is Monday");
break;
case 3: System.out.println("3rd day of a week is Tuesday");
break;
case 4: System.out.println("4th day of a week is wednesday");
break;
case 5: System.out.println("5th day of a week is Thursday");
break;
case 6: System.out.println("6th day of a week is Friday");
break;
case 7: System.out.println("7th day of a week is Saturday");
break;
default: System.out.println("invalid value, value range must between 1-7");
}
}
}